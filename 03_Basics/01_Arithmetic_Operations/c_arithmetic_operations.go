/*
arithmetic operations
	+ 	addition
	-	subtraction
	*	Multiplication
	/	Division
	%	Remainder
*/
package main

import "fmt"

func main() {
	fmt.Println("10 + 3 = ", 10+3)
	fmt.Println("10 - 3 = ", 10-3)
	fmt.Println("10 * 3 = ", 10*3)
	fmt.Println("10 / 3 = ", 10/3) // Quotient
	fmt.Println("10 % 3 = ", 10%3) // Remainder

	/*
				3 ) 10 (3   <-- Quotient  /
					 9
					---
		             1  <- remainder  %

	*/
}
