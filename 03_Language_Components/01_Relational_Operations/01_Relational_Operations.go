package main

import "fmt"

func main() {
	usd := 70
	cand := 49

	fmt.Println("usd < cand  =", usd < cand)
	fmt.Println("usd <= cand =", usd <= cand)
	fmt.Println("usd > cand  =", usd > cand)
	fmt.Println("usd >= cand =", usd >= cand)
	fmt.Println("usd == cand =", usd == cand)
	fmt.Println("usd != cand =", usd != cand)
	// fmt.Println("usd <> cand =", usd <> cand)
}